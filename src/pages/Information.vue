            <h2>Надежность, проблемы и ремонт двигателя <br> БМВ М30Б35/М30Б34</h2>
            <p>До появления в 1992 году серии М60, с конфигурацией V8, ее место занимали большеобъемные рядные шестерки, под названием М30. Несмотря на то, что М30 выпускался одновременно с М20 (тоже рядный 6-ти цилиндровый), 30-я модель имеет больше общего с младшей четверкой М10. Первые М30 появились почти 50 лет назад и с течением времени постоянно дорабатывались и модернизировались. Один из топовых вариантов под названием M30B35/M30B34 был выпущен в 1982 году и предназначался для автомобилей BMW с индексом 35i.
                <br><br> Двигатель BMW M30B35/M30B34 представляет собой рядную шестерку в чугунном блоке цилиндров, таком же, как у M30B30, но коленвал имеет ход поршня 86 мм (против 80 мм у M30B30), диаметр цилиндров увеличен c 89 мм до 92 мм. Высота блока цилиндров не отличается. Длина шатуна — 135 мм, компрессионная высота поршня — 39.85 мм.
                <br> Головка блока цилиндров М30B34/B35 одновальная с 12-ю клапанами, без гидрокомпенсаторов, поэтому каждые 10 тыс. км желательно делать регулировку клапанов. Зазоры (холодный двигатель) на впуск и выпуск — 0.3 мм. Характеристики распределительного вала M30B35: фаза 248, подъем 8 мм.
                <br> Впускной коллектор изменен, используются форсунки производительностью 214 сс.
                <br> Привод ГРМ цепной. Система подачи топлива инжекторная.
                <br> Выпускались различные версии этого мотора: с катализатором, поршнями под степень сжатия 8 и Bosch Motronic 1.0, развивающие 185 л.с. при 5400 об/мин; без катализатора и с поршнями под степень сжатия 10, их отдача 218 л.с. при 5500 об/мин; а также катализаторная версия с поршнями под степень сжатия 9 и с Bosch Motronic 1.3, мощность таких M30B35 достигала 211 л.с. при 5700 об/мин.
