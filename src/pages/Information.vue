            <h2>Проблемы и недостатки двигателей <br> BMW M30B35</h2>
                <p>1. Перегрев. Эта проблема свойственна многим рядным 6-ти цилиндровым двигателям BMW и запускать ее не стоит — поведет ГБЦ. Причина неисправности в грязном радиаторе, помпе, термостате или в воздушных пробках в системе охлаждения.
                <br> 2. Трещины в блоке цилиндров. Образуются вокруг резьб для болтов ГБЦ, по причине не удаления масла из резьбовых колодцев, после разборки двигателя. Симптомы, указывающие на появление трещин в М30: уход антифриза, эмульсия в масле. Можно пытаться решить вопрос ремонтом, но надежней и правильней купить блок цилиндров б/у без трещин.
                <br> Учитывая очень преклонный возраст моторов БМВ М30, к вышеперечисленному добавляется износ всего, что только возможно: чаще всего проблемы возникают в механизме газораспределения, с последующей невозможностью регулировки клапана, это решается заменой эксцентриков, изношенных клапанов, направляющих втулок. Нередки также неисправности связанные с ДМРВ.
                <br> На данный момент все двигатели BMW M30 выкатали свой ресурс, несмотря на то, что он запросто может превышать 500000 км. Также, зачастую автомобили с М30 эксплуатируют достаточно жестко (особенно Е34), это дополнительно накладывает свой отпечаток.
                        Сам по себе BMW M30B35 великолепный двигатель для своего времени, но возраст берет свое. Если хотите купить БМВ и стоит выбор между М30 или М50 (и другими моторами этой серии), стоит брать более свежий М50. </p>
                <h2>Тюнинг двигателя BMW M30B35<br>M30B35 Атмо. Строкер</h2>
                <p>Тюнинговых запчастей на М30 немало и при наличии денег, можно снять с него немного лошадей, но для начала приведите свой мотор в нормальное состояние, если двигатель просит капиталки, то тут никакие валы вам не помогут. После восстановления M30B35 есть два пути: сделать строкер или атмосферник на валах.
                <br> Первый вариант предполагает установку коленвала с ходом поршня 98 мм, коротких шатунов и расточку цилиндров под кованые поршни 93.4 мм. Это обеспечит 4.0 л рабочего объема, расточив до 95 мм, вы получите 4.2 л, больше нельзя. Стоимость подобных китов выше стоимости всего автомобиля.
                <br> Второй путь: вам нужно отфрезеровать ГБЦ для повышения степень сжатия до ~11, доработать каналы и камеры сгорания, также вам понадобится спортивный впуск, распредвал Schrick 284/280 (или другой похожий) с пружинами, усиленные рокеры, 76 мм прямоточный выпуск и настройка мозгов. Все это позволит снять около 240-250 л.с., но поршневая старая, тяжелая, крутится неохотно, и лучше заменить ее на легкую ковку, добавить дросселя от S38. В итоге дешевле купить BMW S38 на дросселях с 340 л.с. и сделать свап.</p>
            <h2>Турбо M30B35</h2>
            <p>Двигатели БМВ М30 с завода имеют очень низкую степень сжатия, поэтому разжимать под наддув не нужно, а значит надуть гораздо проще, чем сделать атмосферник. Купите на eBay дешевый китайский турбо кит для БМВ М30 и установите его на сток мотор. Или раздельно купите китайский Garrett GT35, с коллектором, пайпами 3 дюймовыми, интеркулером, вестгейтом, блоу-оффом, форсунками 630 сс и блоком управления Megasquirt. Надуете 0.8-1 бар и получите под 400 л.с. Китайские турбины живут недолго, несколько тысяч км и нужно менять. Оригинальный 35-й гарретт гораздо лучше и эффективней, но цена отличается в разы.
            <br> Нечто подобное можно собрать, используя компрессор, но с турбиной будет эффективней.</p>
